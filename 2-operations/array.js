// Manipulating the array

// Arrays can function as a stack.
// The "push" and "pop" methods insert and remove variables at/from the end of an array.
// Usage:
// let a = [];
// a.push(1);
// a.pop();

// Another methods that exist on the array is shift and unshift.
// The shift and unshift methods are similar to push and pop,
// the difference is that they operate on the beginning of the array.
// Usage:
// let a = [];
// a.unshift(1);
// a.shift();


